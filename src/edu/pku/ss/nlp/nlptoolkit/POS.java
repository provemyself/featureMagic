package edu.pku.ss.nlp.nlptoolkit;

/**
 * This class is the definition of all the part-of-speech following the
 * "Part-of-Speech Tagging Guidelines for the Penn Treebank Project(3rd
 * Revision, 2nd printing)".
 * 
 * @author 王志伟
 * 
 */
public class POS {
	/**
	 * Coordinating conjunction.This category includes and, but, nor, or, yet(as
	 * in Yet it's cheap,cheap yet good), as well as the mathematical operators
	 * plus, minus, less, times(in the sense of "multiplied by") and over(in the
	 * sense of "divided by"), when they are spelled out.
	 */
	public static final String CC = "CC";
	/**
	 * Cardinal number.
	 */
	public static final String CD = "CD";
	/**
	 * Determiner.
	 */
	public static final String DT = "DT";
	/**
	 * Existential there.
	 */
	public static final String EX = "EX";
	/**
	 * Preposition or subordinating conjunction.
	 */
	public static final String IN = "IN";
	/**
	 * Adjective.
	 */
	public static final String JJ = "JJ";
	/**
	 * Adjective, comparative.
	 */
	public static final String JJR = "JJR";
	/**
	 * Adjective, superlative.
	 */
	public static final String JJS = "JJS";
	/**
	 * List item marker.
	 */
	public static final String LS = "LS";
	/**
	 * Modal.
	 */
	public static final String MD = "MD";
	/**
	 * Noun, singular or mass.
	 */
	public static final String NN = "NN";
	/**
	 * Noun, plural.
	 */
	public static final String NNS = "NNS";
	/**
	 * Proper noun, singular.
	 */
	public static final String NNP = "NNP";
	/**
	 * Proper noun, plural.
	 */
	public static final String NNPS = "NNPS";
	/**
	 * Predeterminer.
	 */
	public static final String PDT = "PDT";
	/**
	 * Possessive ending.
	 */
	public static final String POS = "POS";
	/**
	 * Personal pronoun.
	 */
	public static final String PRP = "PRP";
	/**
	 * Possessive pronoun.
	 */
	public static final String PRP$ = "PRP$";
	/**
	 * Adverb.
	 */
	public static final String RB = "RB";
	/**
	 * Adverb, comparative.
	 */
	public static final String RBR = "RBR";
	/**
	 * Adverb, superlative.
	 */
	public static final String RBS = "RBS";
	/**
	 * Particle. an adverb or a preposition that can combine with a verb to make
	 * a phrasal verb.
	 */
	public static final String RP = "RP";
	/**
	 * Symbol.
	 */
	public static final String SYM = "SYM";
	/**
	 * to.
	 */
	public static final String TO = "TO";
	/**
	 * Interjection.
	 */
	public static final String UH = "UH";
	/**
	 * Verb, base form.
	 */
	public static final String VB = "VB";
	/**
	 * Verb, past tense.
	 */
	public static final String VBD = "VBD";
	/**
	 * Verb, gerund or present participle.
	 */
	public static final String VBG = "VBG";
	/**
	 * Verb, past participle.
	 */
	public static final String VBN = "VBN";
	/**
	 * Verb, non-3rd person singular present.
	 */
	public static final String VBP = "VBP";
	/**
	 * Verb, 3rd person singular present.
	 */
	public static final String VBZ = "VBZ";
	/**
	 * Wh-determiner.
	 */
	public static final String WDT = "WDT";
	/**
	 * Wh-pronoun.This category includes what, who and whom.
	 */
	public static final String WP = "WP";
	/**
	 * Possessive wh-pronoun.
	 */
	public static final String WP$ = "WP$";
	/**
	 * Wh-adverb.
	 */
	public static final String WRB = "WRB";
}
